/*
*	Input		:	None
*	Output		:	Status of setup
*	Description	:	Setup system
*/
uint32_t	sysSetup(void)
{
	/* Clock */
	clkSetup(CLK_SRC_MAIN, CLK_PLL_ON, CLK_FREQ_120MHZ);
	
	/* LED */
	gpioLEDSetup(1);	gpioLEDSetup(2);	gpioLEDSetup(3);	gpioLEDSetup(4);	
	edmcLedSetup();
	
	/* ICDI */
	edmcIcdiEnable();
	edmcIcdiSendSysInfo();

	/* Keypad */
	edmcKpEnable();
	
	/* SD Card */
//	edmcSdcEnable();
	
	/* LCD */
//	edmcLcdEnable();
	
	/* OS */
	edmcOsSetup();
	
	/* Return */
	return 0;
}
